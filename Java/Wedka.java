class Wedka {
	public static void main(String[] args){
		///////////////////
		double a = 0.25; //Grubość żyłki nominalnej
		double b = 200; //Długość żyłki minimalnej
		//-----------------
		double an = 0.14; //Grubość nowej żyłki
		//-----------------
		double ap = 0.3; //Grubość podkładu
		///////////////////
		double v;
		v = Math.PI*Math.pow(a/2, 2)*b;
		System.out.println(v);
		double bn;
		double vn = Math.PI*Math.pow(an/2, 2);
		bn = v/vn;
		System.out.println(bn);
		double bp;
		double vp = v - vn;
		bp = vp/(Math.PI*Math.pow(ap/2, 2));
		System.out.println(bp);
	}
}
